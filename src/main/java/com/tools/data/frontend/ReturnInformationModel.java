package com.tools.data.frontend;

public class ReturnInformationModel {

	private String returnNumber;
	private String returnDate;
	private String orderNumber;
	private String orderDate;
	private String scEmailAddress;
	private String contactEmailAddress;

	public String getReturnNumber() {
		return returnNumber;
	}

	public void setReturnNumber(String returnNumber) {
		this.returnNumber = returnNumber;
	}

	public String getReturnDate() {
		return returnDate;
	}

	public void setReturnDate(String returnDate) {
		this.returnDate = returnDate;
	}

	public String getOrderNumber() {
		return orderNumber;
	}

	public void setOrderNumber(String orderNumber) {
		this.orderNumber = orderNumber;
	}

	public String getOrderDate() {
		return orderDate;
	}

	public void setOrderDate(String orderDate) {
		this.orderDate = orderDate;
	}

	public String getScEmailAddress() {
		return scEmailAddress;
	}

	public void setScEmailAddress(String scEmailAddress) {
		this.scEmailAddress = scEmailAddress;
	}

	public String getContactEmailAddress() {
		return contactEmailAddress;
	}

	public void setContactEmailAddress(String contactEmailAddress) {
		this.contactEmailAddress = contactEmailAddress;
	}

}
